package com.example.paintify.ui

import androidx.compose.ui.graphics.Color

/**
 * Centralized color palette for Paintify's dark theme.
 *
 * Use these colors instead of hard-coding hex values in composables so the
 * app keeps a consistent, professional look and is easy to tweak later.
 */
object PaintifyColors {
    val Background      = Color(0xFF020617)
    val Surface         = Color(0xFF0B1120)
    val SurfaceVariant  = Color(0xFF111827)
    val Accent          = Color(0xFF6366F1)
    val AccentSoft      = Color(0xFF38BDF8)
    val Error           = Color(0xFFF97373)
}
